# Exercício: crie um programa que contêm uma função que receba dois parâmetros inteiros e retorna a média dos dois valores.

'''
Solução: a função "calcula_media" tem dois parâmetros
'''

def calcula_media(primeiro_num, segundo_num):
    media = (primeiro_num + segundo_num) / 2
    return media 


print(calcula_media(10, 20))
